// Generate a COMPACT world land outline for the published reader's route map.
//
// The published reader is one self-contained HTML file with no external URL, so
// it can't fetch the app's 756 KB basemap. This bakes a heavily-simplified land
// silhouette (Natural Earth 50m → land object) down to a few tens of KB that we
// embed once per published document — enough to give every route map real
// coastlines and continents instead of an empty white grid.
//
// Run: node scripts/build-reader-land.mjs
// Output: src/lib/publish/landOutline.ts (a `LAND_OUTLINE` constant).

import { readFileSync, writeFileSync } from "node:fs";
import { feature } from "topojson-client";

const SRC = new URL("../public/basemap/countries-50m.json", import.meta.url);
const OUT = new URL("../src/lib/publish/landOutline.ts", import.meta.url);

// Tuning: coarser tolerance + higher area floor = smaller file, blockier coast.
const TOLERANCE_DEG = 0.55; // Ramer–Douglas–Peucker tolerance in degrees
const MIN_AREA_DEG2 = 1.6; // drop rings whose lon×lat bbox area is below this
const COORD_DECIMALS = 1; // 0.1° grid — plenty for a small context map

const topo = JSON.parse(readFileSync(SRC, "utf8"));
const land = feature(topo, topo.objects.land);

/** Collect every polygon ring (outer + holes ignored — silhouette only). */
function collectRings(geo) {
  const rings = [];
  const addPoly = (poly) => rings.push(poly[0]); // outer ring only
  const walk = (g) => {
    if (!g) return;
    if (g.type === "Polygon") addPoly(g.coordinates);
    else if (g.type === "MultiPolygon") for (const p of g.coordinates) addPoly(p);
    else if (g.type === "GeometryCollection") for (const c of g.geometries) walk(c);
    else if (g.type === "Feature") walk(g.geometry);
    else if (g.type === "FeatureCollection") for (const f of g.features) walk(f.geometry);
  };
  walk(geo);
  return rings;
}

/** Perpendicular distance from p to the segment a→b, in degrees. */
function segDist(p, a, b) {
  const [px, py] = p, [ax, ay] = a, [bx, by] = b;
  const dx = bx - ax, dy = by - ay;
  const len2 = dx * dx + dy * dy;
  if (len2 === 0) return Math.hypot(px - ax, py - ay);
  let t = ((px - ax) * dx + (py - ay) * dy) / len2;
  t = Math.max(0, Math.min(1, t));
  return Math.hypot(px - (ax + t * dx), py - (ay + t * dy));
}

/** Ramer–Douglas–Peucker simplification. */
function rdp(pts, eps) {
  if (pts.length < 3) return pts;
  let maxD = 0, idx = 0;
  for (let i = 1; i < pts.length - 1; i++) {
    const d = segDist(pts[i], pts[0], pts[pts.length - 1]);
    if (d > maxD) { maxD = d; idx = i; }
  }
  if (maxD > eps) {
    const left = rdp(pts.slice(0, idx + 1), eps);
    const right = rdp(pts.slice(idx), eps);
    return left.slice(0, -1).concat(right);
  }
  return [pts[0], pts[pts.length - 1]];
}

function bboxArea(ring) {
  let minX = Infinity, minY = Infinity, maxX = -Infinity, maxY = -Infinity;
  for (const [x, y] of ring) {
    if (x < minX) minX = x;
    if (x > maxX) maxX = x;
    if (y < minY) minY = y;
    if (y > maxY) maxY = y;
  }
  return (maxX - minX) * (maxY - minY);
}

const round = (n) => Number(n.toFixed(COORD_DECIMALS));

const out = [];
for (const ring of collectRings(land)) {
  if (bboxArea(ring) < MIN_AREA_DEG2) continue;
  let simp = rdp(ring, TOLERANCE_DEG);
  if (simp.length < 4) continue;
  // Quantize; drop consecutive duplicates created by rounding.
  const q = [];
  for (const [x, y] of simp) {
    const rx = round(x), ry = round(y);
    const last = q[q.length - 1];
    if (!last || last[0] !== rx || last[1] !== ry) q.push([rx, ry]);
  }
  if (q.length >= 4) out.push(q);
}

const totalPts = out.reduce((n, r) => n + r.length, 0);
const body = `// AUTO-GENERATED by scripts/build-reader-land.mjs — do not edit by hand.
//
// A compact world land silhouette (Natural Earth 50m, public domain), heavily
// simplified (RDP ${TOLERANCE_DEG}°, ${COORD_DECIMALS}-decimal grid, small islands dropped) so the
// self-contained published reader can draw real coastlines behind the route
// without fetching anything. ${out.length} rings, ${totalPts} points.
//
// Each ring is a closed outer boundary as [lon, lat] pairs in degrees.
export const LAND_OUTLINE: [number, number][][] = ${JSON.stringify(out)};
`;

writeFileSync(OUT, body);
console.log(
  `Wrote ${OUT.pathname}: ${out.length} rings, ${totalPts} points, ${(body.length / 1024).toFixed(1)} KB`,
);
